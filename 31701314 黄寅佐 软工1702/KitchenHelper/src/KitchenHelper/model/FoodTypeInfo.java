package KitchenHelper.model;

public class FoodTypeInfo {
	private int foodTypeNo;
	private String foodTypeName;
	private String foodTypeDetail;

	public int getFoodTypeNo() {
		return foodTypeNo;
	}

	public void setFoodTypeNo(int foodTypeNo) {
		this.foodTypeNo = foodTypeNo;
	}

	public String getFoodTypeName() {
		return foodTypeName;
	}

	public void setFoodTypeName(String foodTypeName) {
		this.foodTypeName = foodTypeName;
	}

	public String getFoodTypeDetail() {
		return foodTypeDetail;
	}

	public void setFoodTypeDetail(String foodTypeDetail) {
		this.foodTypeDetail = foodTypeDetail;
	}
}
